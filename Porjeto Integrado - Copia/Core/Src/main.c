// Atualiza a posição da cabeça da cobrinha com base na direção atual

struct Snake {
    Point body[SNAKE_MAX_LENGTH];
    int length;
    Direction direction;
};




    void game_init(struct Snake *snake);
	void update_snake_position(struct Snake *snake);
	void draw_snake(struct Snake *snake);
	int check_collision(struct Snake *snake);

	void game_init(struct Snake *snake);

	void game_init(struct Snake *snake) {
	    // Configurações iniciais do hardware
	    ST7789_Init();

	    // Inicialização da cobrinha
	    snake->length = 1;
	    snake->body[0].x = 120;
	    snake->body[0].y = 120;
	    snake->direction = RIGHT;
	}


	void update_snake_position(struct Snake *snake) {
	// Atualiza a posição da cabeça da cobrinha com base na direção atual

    switch (snake->direction) {
        case UP:
            snake->body[0].y--;
            break;
        case DOWN:
            snake->body[0].y++;
            break;
        case LEFT:
            snake->body[0].x--;
            break;
        case RIGHT:
            snake->body[0].x++;
            break;
    }
    // Atualiza a posição do corpo da cobrinha movendo cada segmento
    for (int i = snake->length - 1; i > 0; i--) {
            snake->body[i] = snake->body[i - 1];
	}

    void draw_snake(struct Snake* snake) {
        // Limpa a tela
        ST7789_FillScreen(BLACK);

        // Desenha cada segmento da cobrinha
        for (int i = 0; i < snake->length; i++) {
            ST7789_DrawPixel(snake->body[i].x, snake->body[i].y, WHITE);
        }

        // Atualiza a tela
        ST7789_Refresh();
    }

    int check_collision(struct Snake *snake) {

        // Verifica colisão com as bordas da tela

        if (snake->body[0].x < 0 || snake->body[0].x >= 240 || snake->body[0].y < 0 || snake->body[0].y >= 240) {
            return 1; // Colisão com as bordas
        }

        // Verifica colisão com o próprio corpo da cobrinha

        for (int i = 1; i < snake->length; i++) {
            if (snake->body[0].x == snake->body[i].x && snake->body[0].y == snake->body[i].y) {
                return 1; // Colisão com o próprio corpo
            }
        }

        return 0; // Sem colisão
    }
}
